public class a29_30_ForLoop_Print10Numbers
{
    public static void main(String args[])
        {
            for(int i=1;i<=10;i++)
            {
                System.out.print(i + " ");
            }
            System.out.println();
            for(int k=10;k>=1;k--)
            {
                System.out.print(k + " ");
            }
            System.out.println();
            int j=1;
            for(;j<=10;){
                System.out.print(j+ " ");
                j++;
            }
            System.out.println();
        }
    }


