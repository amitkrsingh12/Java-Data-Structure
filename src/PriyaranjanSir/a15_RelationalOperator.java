package PriyaranjanSir;
/*
Relational or Comparision Operator

 */
public class a15_RelationalOperator {
    public static void main(String args[]){
        int a=10,b=15;
        System.out.println("Value of a==b   : " +(a==b));
        System.out.println("Value of a<=b   : " +(a<=b));
        System.out.println("Value of a>=b   : " +(a>=b));
        System.out.println("Value of a>b   : " +(a>b));
        System.out.println("Value of a<b   : " +(a<b));
        System.out.println("Value of a!=b   : " +(a!=b));
    }
}
